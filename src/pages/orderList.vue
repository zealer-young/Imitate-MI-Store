<template>
  <div class="order-list">
    <order-header title="订单列表">
      <template v-slot:tip>
        <span>请谨防钓鱼链接或诈骗电话，了解更多></span>
      </template>
    </order-header>
    <div class="wrapper">
      <div class="container">
        <div class="order-box">
          <loading v-if="loading"></loading>
          <div class="order" v-for="(order,index) in list" :key="index">
            <div class="order-title">
              <div class="item-info fl">
                {{order.createTime}}
                <span>|</span>
                {{order.receiverName}}
                <span>|</span>
                订单号：{{order.orderNo}}
                <span>|</span>
                {{order.paymentTypeDesc}}
              </div>
              <div class="item-money fr">
                <span>应付金额：{{order.payment}}</span>
                <span class="money"></span>
                <span>元</span>
              </div>
            </div>
            <div class="order-content clearfix">
              <div class="good-box fl">
                <div class="good-list" v-for="(item,i) in order.orderItemVoList" :key="i">
                  <div class="good-img">
                    <img v-lazy="item.productImage" />
                  </div>
                  <div class="good-name">
                    <div class="p-name">{{item.productName}}</div>
                    <div class="p-money">{{item.totalPrice + 'X' + item.quantity}}元</div>
                  </div>
                </div>
              </div>
              <!-- status:订单状态 0-已取消，10-未付款，20-已付款，40-已发货，50-交易成功，60-交易关闭 -->
              <div class="good-state fr" v-if="order.status == 20">
                <a href="javascript:;">{{order.statusDesc}}</a>
              </div>
              <div class="good-state fr" v-else>
                <a href="javascript:;" @click="goPay(order.orderNo)">{{order.statusDesc}}</a>
              </div>
            </div>
          </div>
          <el-pagination
            v-if="true"
            class="pagination"
            background
            layout="prev,pager,next"
            :pageSize="pageSize"
            :total="total"
            @current-change="handleChange"
          ></el-pagination>
          <div class="load-more" v-if="false">
            <el-button type="primary" :loading="loading" @click="loadMore">加载更多</el-button>
          </div>
          <div
            class="scroll-more"
            v-infinite-scroll="scrollMore"
            infinite-scroll-disabled="true"
            infinite-scroll-distance="410"
            v-if="false"
          >
            <!-- 
            v-infinite-scroll="scrollMore" 当滚动时会触发该方法
            infinite-scroll-disabled="busy" true时禁用，false时释放
            infinite-scroll-distance="10"  距离底部多高时使用，"10"代表10px
            -->
            <img src="/imgs/loading-svg/loading-bubbles.svg" alt v-show="loading" />
          </div>
          <no-data v-if="!loading && list.length == 0"></no-data>
        </div>
      </div>
    </div>
  </div>
</template>
<script>
import OrderHeader from "./../components/OrderHeader";
import Loading from "./../components/Loading";
import NoData from "./../components/NoData";
import { Pagination, Button } from "element-ui"; //element-ui Pagination分页器 Button 按钮
import infiniteScroll from "vue-infinite-scroll"; // 滚动分页器
export default {
  name: "order-list",
  components: {
    OrderHeader,
    Loading,
    NoData,
    [Pagination.name]: Pagination,
    [Button.name]: Button
  },
  directives: {
    infiniteScroll
  }, //滚动分页器的配置项
  data() {
    return {
      list: [],
      loading: false,
      pageSize: 10,
      pageNum: 1,
      total: 0, //分页器方法使用的变量
      showNextPage: true, //加载更多方法：是否显示按钮
      busy: false //滚动加载方法：是否触发
    };
  },
  mounted() {
    this.getOrderList();
  },
  methods: {
    getOrderList() {
      this.loading = true;
      this.busy = true;
      this.axios
        .get("/orders", {
          params: {
            pageSize: 10,
            pageNum: this.pageNum
          }
        })
        .then(res => {
          this.loading = false;
          this.list = this.list.concat(res.list);
          this.total = res.total;
          this.showNextPage = res.hasNextPage;
          this.busy = false;
        })
        .catch(() => {
          this.loading = false;
        });
    },
    //去支付
    goPay(orderNo) {
      // 三种路由跳转方式
      // 第一种 this.$router.push('/order/pay');
      //第二种
      /*this.$router.push({
          name:'order-pay',//name这里指路由的名称
          query:{
            orderNo//这里是传递的参数
          }
        })
        */
      //第三种
      this.$router.push({
        path: "/order/pay", //path是路径
        query: {
          orderNo
        }
      });
      //第二种和第三种的区别是：第二种使用的是路由的名称跳转；第三种使用的是路径跳转
      // query传参和params传参的区别是：query传参可以将参数添加到地址栏里面去
    },
    //分页第一种方法：分页器
    handleChange(pageNum) {
      this.pageNum = pageNum;
      this.getOrderList();
    },
    //分页第二种方法：加载更多按钮
    loadMore() {
      this.pageNum++;
      this.getOrderList();
    },
    //分页第三种方法：滚动加载，通过npm插件实现
    scrollMore() {
      this.busy = true;
      setTimeout(() => {
        this.pageNum++;
        this.getList();
      }, 500);
    },
    //专门给scrollMore使用
    getList() {
      this.loading = true;
      this.axios
        .get("/orders", {
          params: {
            pageSize: 10,
            pageNum: this.pageNum
          }
        })
        .then(res => {
          this.list = this.list.concat(res.list);
          this.loading = false;
          if (res.hasNextPage) {
            //判断是否有下一页
            this.busy = false;
          } else {
            this.busy = true;
          }
        });
    }
  }
};
</script>
